package com.clyde.scala.old

class Animal {
  println("Animal")
}

class Mamal extends Animal {
  println("Mamal")
}

class Mouse extends Mamal {
  println("Mouse")
}

class Bird extends Animal {
  println("Bird")
}

class Parrot extends Bird {
  println("Parrot")
}

class Owl extends Bird {
  println("Owl")
}